class Solution{
    public:
    int searchInsertK(vector<int> arr, int n, int k)
    {
        int x=lower_bound(arr.begin(),arr.end(),k)-arr.begin();
        
        if(arr[n-1]<k) return n;
        if(x!=n) return x;
        return x-1;
    }
};

